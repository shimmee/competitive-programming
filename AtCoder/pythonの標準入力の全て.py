#############################################################################
# 整数がn行にまたがる数列みたいなのの受け取り: 数列の長さがn
#############################################################################
n = int(input())
A = [int(input()) for _ in range(n)]

4 # これがn
2 # A1
5 # A2
1 # A3
5 # A4

#############################################################################
# 空白あり横並び整数がn行にまたがるやつ。行列とかの受け取り
#############################################################################
